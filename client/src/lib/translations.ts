export type TranslationKey = {
  [Section in keyof typeof translations]: `${Section}.${string & keyof (typeof translations)[Section]}`;
}[keyof typeof translations];

export function getTranslation(key: string, language: Language): string {
  const parts = key.split(".");
  if (parts.length !== 2) return key;

  const [section, field] = parts;
  const sectionObj = (translations as any)[section];
  if (!sectionObj) return key;

  const entry = sectionObj[field];
  if (!entry) return key;

  return entry[language] || entry["es"] || key;
}

export function t(key: string, language: Language, replacements?: Record<string, string | number>): string {
  let result = getTranslation(key, language);

  if (replacements) {
    for (const [placeholder, value] of Object.entries(replacements)) {
      result = result.replace(`{${placeholder}}`, String(value));
    }
  }

  return result;
}

